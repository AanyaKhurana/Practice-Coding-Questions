#-------------------------------------------------------------------------------

class Solution:
    def reverseString(self, s):
        """
        :type s: str
        :rtype: str
        """
        l = list(s)
        for i in range(len(s) // 2):
            l[i], l[~i] = l[~i], l[i]
        return "".join(l)

